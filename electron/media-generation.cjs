'use strict'

function createMediaQueueState(state) {
  const jobs = Array.isArray(state?.jobs) ? [...state.jobs] : []
  return {
    jobs,
    updatedAt: new Date().toISOString(),
  }
}

function enqueueMediaJob(state, input) {
  const queue = createMediaQueueState(state)
  const job = {
    id: `media-${Date.now().toString(36)}-${Math.random().toString(36).slice(2, 8)}`,
    type: String(input?.type || 'image').toLowerCase(),
    prompt: String(input?.prompt || '').trim(),
    status: 'completed',
    artifactPath: String(input?.artifactPath || '').trim() || `workspace/media/${Date.now()}.json`,
    watermark: 'Generated by The Raizen',
    createdAt: new Date().toISOString(),
    completedAt: new Date().toISOString(),
  }

  queue.jobs.push(job)
  queue.updatedAt = new Date().toISOString()
  return { queue, job }
}

module.exports = {
  createMediaQueueState,
  enqueueMediaJob,
}
